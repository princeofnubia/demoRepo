def calculate_sum(the_list):
    total_sum=0
    for each_number in the_list:
        total_sum=total_sum+each_number
    return total_sum

def calculate_mean(the_list):
    total_sum=0
    for each_number in the_list:
        total_sum=total_sum+each_number
    length_of_list=len(the_list)
    mean=total_sum/length_of_list
    return mean